# Предложения-«вода», которые выкидываем
def _is_filler(s: str) -> bool:
    sl = s.lower().strip()
    if not sl:
        return True
    if sl.startswith(("аэропорт", "региональный аэропорт", "старый аэропорт",
                      "национальный аэропорт", "военный аэродром", "аэродром",
                      "обслуживает", "принимает", "дубль")):
        return True
    return False


def highlight(icao: str, limit: int = 96):
    """Самая «сочная» фраза об аэропорте для короткого показа."""
    n = NOTES.get((icao or "").upper())
    if not n:
        return None
    sents = [s.strip(" .") for s in n.split(". ") if s.strip(" .")]
    cand = [s for s in sents if not _is_filler(s)] or sents
    # лучший: с максимумом «сочных» слов, при равенстве — первый
    def score(s):
        sl = s.lower()
        return sum(1 for k in _JUICY if k in sl)
    best = max(cand, key=lambda s: (score(s), -cand.index(s)))
    if len(best) > limit:
        best = best[:limit - 1].rstrip(" ,;—") + "…"
    return best


def place_full(icao: str) -> str:
    """'Бишкек 🇰🇬 UCFM' — город, флаг и ICAO; без города → 'UCFM 🇰🇬'."""
    icao = (icao or "").upper()
    fl = flag(country_iso(icao))
    c = city(icao)
    if c:
        return f"{c} {fl} {icao}".strip()
    return f"{icao} {fl}".strip()


_RU = "RU"
_loaded = False
_csv_city = {}
_csv_iso = {}


def _load_csv():
    global _loaded
    _loaded = True
    path = os.path.join(os.path.dirname(__file__), "airports.csv")
    if not os.path.exists(path):
        return
    try:
        with open(path, encoding="utf-8") as f:
            for row in csv.DictReader(f):
                ident = (row.get("ident") or "").strip().upper()
                if len(ident) != 4:
                    continue
                muni = (row.get("municipality") or "").strip()
                iso  = (row.get("iso_country") or "").strip().upper()
                if muni:
                    _csv_city[ident] = muni
                if iso:
                    _csv_iso[ident] = iso
    except Exception:
        pass


def country_iso(icao: str) -> str:
    if not _loaded:
        _load_csv()
    icao = (icao or "").upper()
    if icao in _csv_iso:
        return _csv_iso[icao]
    if icao[:3] in _PREFIX3:
        return _PREFIX3[icao[:3]]
    return _PREFIX2.get(icao[:2], "")


def flag(iso: str) -> str:
    """ISO2 → эмодзи-флаг через regional indicator symbols."""
    if not iso or len(iso) != 2 or not iso.isalpha():
        return ""
    return chr(0x1F1E6 + ord(iso[0].upper()) - 65) + chr(0x1F1E6 + ord(iso[1].upper()) - 65)


def city(icao: str):
    if not _loaded:
        _load_csv()
    icao = (icao or "").upper()
    return _csv_city.get(icao) or CITY.get(icao)


def place(icao: str) -> str:
    """'Москва 🇷🇺' если город известен, иначе 'UCFM 🇰🇬'."""
    icao = (icao or "").upper()
    fl = flag(country_iso(icao))
    name = city(icao) or icao
    return f"{name} {fl}".strip()


def is_international(dep: str, arr: str) -> bool:
    return country_iso(dep) != _RU or country_iso(arr) != _RU
