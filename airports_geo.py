def coords(icao):
    """(lat, lon) или None, если аэропорт неизвестен."""
    return COORDS.get((icao or "").upper())


def distance_km(dep, arr):
    """Расстояние по большому кругу в км. None — если координат нет."""
    a, b = coords(dep), coords(arr)
    if not a or not b:
        return None
    la1, lo1 = math.radians(a[0]), math.radians(a[1])
    la2, lo2 = math.radians(b[0]), math.radians(b[1])
    h = (math.sin((la2 - la1) / 2) ** 2
         + math.cos(la1) * math.cos(la2) * math.sin((lo2 - lo1) / 2) ** 2)
    return 2 * 6371.0088 * math.asin(math.sqrt(h))


def eta_minutes(km):
    """
    Оценка времени в пути, мин: ~45 мин на руление/набор/снижение
    + крейсер ~850 км/ч. Округление до 5 мин.
    Сверено с реальным расписанием: SVO-LED 1ч30м, SVO-SVX 2ч25м, SVO-OVB 4ч05м.
    """
    return int(round((45 + km / 14.17) / 5) * 5)


def fmt_km(km):
    return "{:,}".format(int(round(km))).replace(",", " ") + " км"
